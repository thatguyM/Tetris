def main(win):
    global pause
    h_score = high_score()
    locked_positions = {}
    grid = create_grid(locked_positions)

    change_piece = False
    run = True
    current_piece = get_shape()
    next_piece = get_shape()
    clock = pygame.time.Clock()
    fall_time = 0
    fall_speed = 0.27
    level_time = 0
    score = 0

    while run:
        grid = create_grid(locked_positions)
        fall_time = fall_time + clock.get_rawtime()
        level_time += clock.get_rawtime()
        clock.tick()

        if level_time/1000 > 5:
            level_time = 0
            if fall_speed > 0.12:
                fall_speed -= 0.005

        if fall_time/1000 > fall_speed:
            fall_time = 0
            current_piece.y += 1
            if not (valid_space(current_piece, grid)) and current_piece.y > 0:
                current_piece.y -= 1
                change_piece = True

        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                run = False
                pygame.display.quit()
                quit()


            if event.type == pygame.KEYDOWN:
                if event.key == pygame.K_LEFT:
                    current_piece.x -= 1
                    if not(valid_space(current_piece, grid)):
                        current_piece.x += 1
                if event.key == pygame.K_SPACE:
                    pause = True
                    paused(win)
                if event.key == pygame.K_RIGHT:
                    current_piece.x += 1
                    if not(valid_space(current_piece, grid)):
                        current_piece.x -= 1
                if event.key == pygame.K_DOWN:
                    current_piece.y += 1
                    if not(valid_space(current_piece, grid)):
                        current_piece.y -= 1
                if event.key == pygame.K_UP:
                    current_piece.rotation += 1
                    if not(valid_space(current_piece, grid)):
                        current_piece.rotation -= 1

        shape_pos = convert_shape_format(current_piece)

        for i in range(len(shape_pos)):
            x, y = shape_pos[i]
            if y > -1:
                grid[y][x] = current_piece.color

        if change_piece:
            for pos in shape_pos:
                p = (pos[0], pos[1])
                locked_positions[p] = current_piece.color
            current_piece = next_piece
            next_piece = get_shape()
            change_piece = False
            score += clear_rows(grid, locked_positions) * 10


        draw_window(win, grid, score, h_score)
        draw_next_shape(next_piece, win)
        pygame.display.update()

        if check_lost(locked_positions):
            game_over(win, score)
            #draw_text_middle(win, "GAME OVER!", 80 , (255,255,255))
            #pygame.display.update()
            #pygame.time.delay(2000)
            run = False
            update_score(score)

def paused(win):
    global pause
    while pause:
        win.fill((0,0,0))
        draw_text_middle(win, 'Paused - Press Space to Continue', 30, (255,255,255))
        pygame.display.update()
        for event in pygame.event.get():
            if event.type == pygame.KEYDOWN:
                if event.key == pygame.K_SPACE:
                    unpause()

def unpause():
    global pause
    pause = False

def button(msg, x, y, w, h, ic, ac, action=None):
    mouse = pygame.mouse.get_pos()
    click = pygame.mouse.get_pressed()

    if x + w > mouse[0] > x and y + h > mouse[1] > y:
        pygame.draw.rect(win, ac, (x, y, w, h))
        if click[0] == 1 and action != None:
            if action == "play":
                main(win)
            elif action == "quit":
                pygame.quit()
                quit()

    else:
        pygame.draw.rect(win, ic, (x, y, w, h))

    font = pygame.font.SysFont('comicsans', 30)
    label = font.render(msg, 1, (255,255,255))
    win.blit(label,((x-70) + (w/2), y + (h/2)))

def game_over(win, score):
    while True:
        pygame.display.update()
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.quit()
                quit()

        win.fill((0,0,0))
        draw_text(win, "GAME OVER!", 100, (255,255,255), 180, 180)
        draw_text(win, "Score: " + str(score), 60, (255,255,255), 300, 270)
        button("PLAY AGAIN", 350,330,150,50, black, grey, "play")
        button("QUIT", 350,390,150,50, black, grey, "quit")

        pygame.display.update()

